"""Benchmark test suite for evaluating retrieval accuracy."""

import pytest
import structlog
from agent_rag.agent.graph import graph
from tests.benchmark_data import BENCHMARK

logger = structlog.get_logger()

@pytest.mark.asyncio
@pytest.mark.parametrize("case", BENCHMARK)
async def test_benchmark_query(db_session, groq_client, case):
    """
    Test a single benchmark query through the agent pipeline.
    Validates that the expected document and page are present in the retrieved sources.
    """
    initial_state = {
        "session": db_session,
        "groq_client": groq_client,
        "query": case["query"],
        "iteration": 0,
        "timings": {},
        "token_usage": {}
    }
    
    if not case["query"]:
        pytest.skip("Empty query generated by benchmark script")
    
    # Run the full agent pipeline
    result = await graph.ainvoke(initial_state)
    
    search_results = result.get("search_results", [])
    
    assert len(search_results) > 0, "No search results returned"
    
    expected_doc = case["expected_document_title"]
    expected_page = case["expected_page_number"]
    
    # Check retrieval success
    doc_found = False
    page_found = False
    
    for r in search_results:
        if r.source_doc_title == expected_doc:
            doc_found = True
            if r.source_page_number == expected_page:
                page_found = True
                
    # We log errors for observability but use asserts so pytest tracks pass/fail rate
    if not doc_found:
        logger.error(f"Failed to find document. Expected: {expected_doc}", query=case["query"])
    elif not page_found:
        logger.warning(f"Found document, but failed to find page. Expected: {expected_page}", query=case["query"])
        
    assert doc_found, f"Expected document '{expected_doc}' not found in top results"
    assert page_found, f"Expected page {expected_page} not found in top results for document '{expected_doc}'"
